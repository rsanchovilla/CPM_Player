; 20 AUG 79. MOVED IT.
;
; CREATE AND DELETE - TWO SMALL CPM CALLS.
;
	ORG	56A0H
BDOS	EQU	5
CREATE:	PUSH	B
	PUSH	D
	PUSH	H
	MVI	C,22
	CALL	BDOS
	POP	H
	POP	D
	POP	B
	RET
;
DELETE: PUSH	B
	PUSH	D
	PUSH	H
	MVI	C,19
	CALL	BDOS
	POP	H
	POP	D
	POP	B
	RET
;30 AUG 79 - CORRECT ERROR IN FPOP.
;
; FPUSH PUSHES THE CONTENTS OF DE ON THE FELIX STACK.
;
FSTACK	EQU	112H
	ORG	56BAH
FPUSH:	PUSH	H
	LHLD	FSTACK
	DCX	H
	MOV	M,D
	DCX	H
	MOV	M,E
	SHLD	FSTACK
	POP	H
	RET
;
FPOP:	PUSH	H
	LHLD	FSTACK
	MOV	E,M
	INX	H
	MOV	D,M
	INX	H
	SHLD	FSTACK
	POP	H
	RET
