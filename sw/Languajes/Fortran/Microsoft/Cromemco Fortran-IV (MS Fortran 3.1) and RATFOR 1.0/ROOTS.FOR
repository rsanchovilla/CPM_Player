	PROGRAM ROOTS
	DIMENSION A(10)
C
C	THIS PROGRAM WILL PRINT
C	A TABLE OF SQUARE ROOTS
C
	WRITE (5,920)
	WRITE (5,930)
	DO 20 I=0,9
	DO 10 J=0,9
C
C	SPECIFY AN INTEGER BETWEEN 0 AND 99
C
	B=FLOAT(10*I+J)
C
C	TAKE ITS SQUARE ROOT AND
C	STORE THE VALUE IN AN ARRAY
C
	A(J)=SQRT(B)
10	CONTINUE
20	WRITE (5,960) I,(A(K),K=0,9)
	WRITE (5,970)
C
C	FORMAT THE TABLE HEADING
C
920	FORMAT(////25X,'TABLE OF SQUARE ROOTS'/)
930	FORMAT(/8X,'0',6X,'1',6X,'2',6X,'3',6X,'4',6X,
     +	'5',6X,'6',6X,'7',6X,'8',6X,'9'/)
C
C	FORMAT THE ARRAY OUTPUT
C
960	FORMAT(1X,I1,'-',10(F7.3))
970	FORMAT(///)
	END
