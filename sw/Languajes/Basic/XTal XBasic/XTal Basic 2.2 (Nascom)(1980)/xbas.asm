;
; BUILD STANDALONE OR CP/M VERSION
;

	.Z80
	ASEG
        ORG  0100H
        
BDOS	EQU  0005h

	; SET CONIN COUN OUT BIOS VECTORS

	LD HL,(1)	;BIOS WARM BOOT -> HL
        LD BC,6		;BIOS CONIN -> HL
        ADD HL,BC
        INC HL
        LD E,(HL)
        INC HL
        LD D,(HL)
        LD (CONIN+1),DE	;STORE ADR. FOR CONIN

	LD HL,(1)	;BIOS WARM BOOT -> HL
        LD BC,9		;BIOS CONOU -> HL
        ADD HL,BC
        INC HL
        LD E,(HL)
        INC HL
        LD D,(HL)
        LD (CONOUT+1),DE ;STORE ADR. FOR CONOUT

	JP 01004H	; Jump to start of XBAS

CPMOUT:	JP  0		; EXIT TO CP/M

CONIN:	JP  0           ;CALL BIOS CONSOLE IN
CONOUT:	JP  0           ;CALL BIOS CONSOLE OUT


CO:	PUSH AF		; CP/M CONSOLE CHAR OUT
	PUSH HL
	PUSH DE
	PUSH BC
	LD C,A
	CP 13
	JP NZ,CO2	; JMP if no CR 
	CALL CONOUT
	LD C,10
CO2:	CALL CONOUT	
	POP BC
	POP DE
	POP HL
	POP AF
	OR A		; Clear carry
	RET

	
	RET

CI:	PUSH HL		; CP/M CONSOLE CHAR IN IF AVAILABLE
	PUSH DE
	PUSH BC
	LD E,255	
	LD C,6
	CALL BDOS	; BDOS function 6 (C_RAWIO) - Direct console I/O
	OR A
	JP Z,CI2	; JMP IF NO KEY PRESSED
	SCF		; SET CARRY
CI2:	POP BC
	POP DE
	POP HL
	RET

	; CSAVE FILENAME -> save tokenized basic program on CP/M file
	
CSAVE:	CALL OPENFW
	OR A
	JP NZ,2A77H 	; Tape Error
	LD A,(0CB7H)	; Save End of basic program
	CALL SAV1
	LD A,(0CB7H+1)	; 0cb7=TXTUNF	End of basic program
	CALL SAV1
	LD HL,(0C8CH)	; 0c8c=TEXT	Start of basic program
CS1:	LD A,(HL)
	INC HL
	PUSH HL	
	CALL SAV1
	POP HL
	OR A
	JP NZ,2A77H 	; Tape Error
	LD DE,(0CB7H)	; 0cb7=TXTUNF	End of basic program
	CALL 1546H	; call CPHLDE
	JP C,CS1	; end of prog not reached -> continue saving
	CALL SAV2
	OR A
	JP NZ,2A77H 	; Tape Error
	JP 1355H	; go to READY

CLOAD:	CALL OPENFR
	OR A
	JP NZ,2A77H 	; Tape Error
	CALL 0140BH	; call NEW basic command
	CALL LOAD1
	JP C,13D0H 	; Tape Error/EOF
	LD (0CB7H),A	; Load End of basic program
	CALL LOAD1
	JP C,13D0H 	; Tape Error/EOF
	LD (0CB7H+1),A
	LD HL,(0C8CH)	; 0c8c=TEXT	Start of basic program
LD1: 	PUSH HL
	CALL LOAD1
	POP HL
	JP C,13D0H 	; Tape Error/EOF
	LD (HL),A
	INC HL
	LD DE,(0CB7H)	; 0cb7=TXTUNF	End of basic program
	CALL 1546H	; call CPHLDE
	JP C,LD1	; end of prog not reached -> continue loading
	JP 13D0H	; end of load

;
; OPENF/CLOSEF FUNCTIONS FOR LOAD/SAVE FILE UNDER CPM 
; RETURNS 0 ON OK, NOT ZERO ON I/O ERROR
;
        .Z80

CNTRLZ	EQU	1AH		;control-z (end-of-file mark)

LOAD1:	LD	A,(FCB)		;READ CHAR FROM FILE
	CP	255		;IF (FCB)=FF THERE IS NO FILE OPEN
	JP	Z,LDBRK		;IF NO FILE OPEN END TAPE READ
	CALL	TYPE1		;GET CHAR FROM FILE C=CHAR, A=0 OK, A<>0 ERROR/EOF
	OR	A
	JP	NZ,LDBRK	;IF ERROR/EOF->RETURN ^C TO CALLER
	LD	A,C
	RET
LDBRK:	SCF
	RET	
        
CLOSEF: LD	A,(FCB)		;IF (FCB)=FF THERE IS NO FILE OPEN
	CP	255
	RET	Z
	CALL	CLOSFCB		;  ELSE CLOSE FILE IN FCB
CLRFCB:	LD	A,255
	LD	(FCB),A
	RET
        
OPENFR:	CALL	CONVERT		;convert file name.
	OR	A
	JP	NZ,TYPERR	;wild cards not allowed.
	CALL	OPENFCB		;open the file.
	CP	255
	JP	Z,TYPERR	;not there?
	LD	HL,NBYTES	;initialize byte counter.
	LD	(HL),0FFH	;set to read first sector.	
	LD 	A,0		;return A=0 -> OK
	RET
	
TYPE1:	LD	HL,NBYTES
TYPE2:	LD	A,(HL)		;have we written the entire sector?
	CP	128
	JP	C,TYPE3
	PUSH	HL		;yes, read in the next one.
	CALL	READFCB
	POP	HL
	OR	A
	JP	NZ,TYPERR	;end or error?
	XOR	A		;ok, clear byte counter.
	LD	(HL),A
TYPE3:	INC	(HL)		;count this byte.
	LD	HL,TBUFF	;and get the (A)th one from the buffer (TBUFF).
	CALL	ADDHL
	LD	A,(HL)
	LD      C,A
	LD 	A,0		;return A=0 -> OK
	RET			; C=CHAR READ
;
;   Get here on an end of file or read error or open error.
;
TYPERR2:CALL	CLOSFCB
TYPERR:	CALL	CLRFCB
  	LD	A,255		;eof found
        RET
;
;   Add (HL)=(HL)+(A)
;
ADDHL:	ADD	A,L
	LD	L,A
	RET	NC		;take care of any carry.
	INC	H
	RET	
;
;   Convert the name in (hl) to (FCB).
;   Format a file name (convert * to '?', etc.). On return,
; (A)=0 is an unambigeous name was specified. Enter with (A) equal to
; the position within the fcb for the name (either 0 or 16).
;
CONVERT:EX	DE,HL
	LD	HL,FCB
	LD	A,(DE)		;get first character of filename
	OR	A
	JP	NZ,CONVRT1
	LD	A,1		;error: empty filename
	RET
CONVRT1:SBC	A,'A'-1		;might be a drive name, convert to binary.
	LD	B,A		;and save.
	INC	DE		;check next character for a ':'.
	LD	A,(DE)
	CP	':'
	JP	Z,CONVRT2
	DEC	DE		;nope, move pointer back to the start of the line.
	LD	A,0             ;default drive
	LD	(HL),A
	JP	CONVRT3
CONVRT2:LD	(HL),B
	INC	DE
;
;   Convert the basic file name.
;
CONVRT3:LD	B,08H
CONVRT4:CALL	CHECK
	JP	Z,CONVRT8
	INC	HL
	CP	'*'		;note that an '*' will fill the remaining
	JP	NZ,CONVRT5	;field with '?'.
	LD	(HL),'?'
	JP	CONVRT6
CONVRT5:LD	(HL),A
	INC	DE
CONVRT6:DEC	B
	JP	NZ,CONVRT4
CONVRT7:CALL	CHECK		;get next delimiter.
	JP	Z,GETEXT
	INC	DE
	JP	CONVRT7
CONVRT8:INC	HL		;blank fill the file name.
	LD	(HL),' '
	DEC	B
	JP	NZ,CONVRT8
;
;   Get the extension and convert it.
;
GETEXT:	LD	B,03H
	CP	'.'
	JP	NZ,GETEXT5
	INC	DE
GETEXT1:CALL	CHECK
	JP	Z,GETEXT5
	INC	HL
	CP	'*'
	JP	NZ,GETEXT2
	LD	(HL),'?'
	JP	GETEXT3
GETEXT2:LD	(HL),A
	INC	DE
GETEXT3:DEC	B
	JP	NZ,GETEXT1
GETEXT4:CALL	CHECK
	JP	Z,GETEXT6
	INC	DE
	JP	GETEXT4
GETEXT5:INC	HL
	LD	(HL),' '
	DEC	B
	JP	NZ,GETEXT5
GETEXT6:LD	B,3
GETEXT7:INC	HL
	LD	(HL),0
	DEC	B
	JP	NZ,GETEXT7
;
; Check to see if this is an ambigeous file name specification.
; Set the (A) register to non zero if it is.
;
	LD	BC,11		;set name length.
	LD	HL,FCB	
GETEXT8:INC	HL
	LD	A,(HL)
	CP	'?'		;any question marks?
	JP	NZ,GETEXT9
	INC	B		;count them.
GETEXT9:DEC	C
	JP	NZ,GETEXT8
	LD	A,B
	OR	A
	RET     NZ		;do niot allow ? in filename
	LD 	A,(FCB)
	OR	A
	JP	Z,GETEXTA
	DEC	A
	LD	E,A
	LD	C,14		;DSKSEL
	CALL	BDOS		
	OR	A
	RET	NZ		;not ready?
GETEXTA:LD	DE,TBUFF
	LD	C,26		;SETDMA
	CALL 	BDOS
	RET	
;
; Check character at (DE) for legal command input. Note that the
; zero flag is set if the character is a delimiter.
;
CHECK:	LD	A,(DE)
	OR	A
	RET	Z
	CP	'='
	RET	Z
	CP	'_'
	RET	Z
	CP	'.'
	RET	Z
	CP	':'
	RET	Z
	CP	';'
	RET	Z
	CP	'<'
	RET	Z
	CP	'>'
	RET	Z
	RET	
;
;   Routine to open file at (FCB).
;
OPENFCB:XOR	A		;clear the record number byte at fcb+32
	LD	(FCB+32),A
	LD	DE,FCB
	LD	C,15
	CALL 	BDOS
	RET
;
;   Routine to read file at (FCB).
;
READFCB:LD	DE,FCB
	LD	C,20
	CALL 	BDOS
	RET

WRTFCB: LD	DE,FCB
	LD	C,21
	CALL 	BDOS
	RET
;
;   Routine to close file at (FCB).
;
CLOSFCB:LD	DE,FCB
	LD	C,16
	CALL 	BDOS
	RET

DELFCB: LD	DE,FCB
	LD	C,19
	CALL 	BDOS
	RET

CREAFCB:LD	DE,FCB
	LD	C,22
	CALL 	BDOS
	RET

;
; File control block setup by the CCP.
;
FCB	EQU	5CH		;default file control block.
TBUFF	EQU	80H		;i/o buffer and command line storage.
NBYTES:	DW	0		;byte counter used by tape command
CH:	DB	0		;save char

OPENFW:	CALL	CONVERT		;convert file name.
	JP	NZ,TYPERR	;wild cards not allowed.
	CALL	DELFCB		;delete file
	CALL	CREAFCB		;and create it again.
	CP	255
	JP	Z,TYPERR	;can't create?
	XOR	A		;clear record number byte.
	LD	(FCB+32),A
	LD	(NBYTES),A
	RET

SAV1:	LD	(CH),A
	LD	A,(NBYTES)
	CP	128
	JP	NZ,SAV1A
	CALL	WRTFCB
	OR	A
	JP	NZ,TYPERR2
	LD	(NBYTES),A
SAV1A:	LD	HL,NBYTES
	LD	A,(HL)
	INC 	(HL)
	LD	HL,TBUFF
	CALL	ADDHL
	LD	A,(CH)
	LD	(HL),A
	LD      A,0
	RET
	
;
;   Get here after writing all of the file.
;
SAV2:	LD	A,(NBYTES)
	OR	A
	JP	Z,SAV2A
	CALL	WRTFCB
	OR	A
	JP	NZ,TYPERR2	
SAV2A:	LD	DE,FCB		;now close the file.
	CALL	CLOSFCB
	CP	255
	JP	Z,TYPERR
	LD	A,0
	RET


